<template>
  <div class="app-container">
    <div class="query-container">
      <el-form :model="queryParams" ref="queryRef" :inline="true">
        <el-form-item label="登录地址" prop="ipaddr">
          <el-input
              v-model="queryParams.ipaddr"
              placeholder="请输入登录地址"
              clearable
              style="width: 200px"
              @keyup.enter="handleQuery"
          />
        </el-form-item>
        <el-form-item label="用户名称" prop="userName">
          <el-input
              v-model="queryParams.userName"
              placeholder="请输入用户名称"
              clearable
              style="width: 200px"
              @keyup.enter="handleQuery"
          />
        </el-form-item>
        <el-form-item>
          <el-button type="primary" icon="Search" @click="handleQuery">搜索</el-button>
          <el-button icon="Refresh" @click="resetQuery">重置</el-button>
        </el-form-item>
      </el-form>
    </div>
    <div class="table-container">
      <el-table
          v-loading="loading"
          :data="onlineList.slice((pageNum - 1) * pageSize, pageNum * pageSize)"
          style="width: 100%;"
      >
        <el-table-column label="序号" width="50" type="index" align="center">
          <template #default="scope">
            <span>{{ (pageNum - 1) * pageSize + scope.$index + 1 }}</span>
          </template>
        </el-table-column>
        <el-table-column label="会话编号" align="center" prop="tokenId" :show-overflow-tooltip="true"/>
        <el-table-column label="登录名称" align="center" prop="userName" :show-overflow-tooltip="true"/>
        <el-table-column label="所属部门" align="center" prop="deptName" :show-overflow-tooltip="true"/>
        <el-table-column label="主机" align="center" prop="ipaddr" :show-overflow-tooltip="true"/>
        <el-table-column label="登录地点" align="center" prop="loginLocation" :show-overflow-tooltip="true"/>
        <el-table-column label="操作系统" align="center" prop="os" :show-overflow-tooltip="true"/>
        <el-table-column label="浏览器" align="center" prop="browser" :show-overflow-tooltip="true"/>
        <el-table-column label="登录时间" align="center" prop="loginTime" width="180">
          <template #default="scope">
            <span>{{ parseTime(scope.row.loginTime) }}</span>
          </template>
        </el-table-column>
        <el-table-column label="操作" align="center" class-name="small-padding fixed-width">
          <template #default="scope">
            <el-button link type="primary" icon="Delete" @click="handleForceLogout(scope.row)" v-hasPermi="['monitor:online:forceLogout']">
              强退
            </el-button>
          </template>
        </el-table-column>
      </el-table>

      <pagination v-show="total > 0" :total="total" v-model:page="pageNum" v-model:limit="pageSize"/>
    </div>
  </div>
</template>

<script setup lang="ts" name="Online">
import { ref, getCurrentInstance, onMounted } from 'vue';
import { forceLogout, list as initData } from "@/api/monitor/online";

interface OnlineUser {
  tokenId: string;
  userName: string;
  deptName: string;
  ipaddr: string;
  loginLocation: string;
  browser: string;
  os: string;
  loginTime: string;
  [key: string]: any;
}

interface QueryParams {
  ipaddr?: string;
  userName?: string;
  [key: string]: any;
}

const { proxy } = getCurrentInstance()!;

const onlineList = ref<OnlineUser[]>([]);
const loading = ref<boolean>(true);
const total = ref<number>(0);
const pageNum = ref<number>(1);
const pageSize = ref<number>(10);

const queryParams = ref<QueryParams>({
  ipaddr: undefined,
  userName: undefined
});

/** 查询登录日志列表 */
function getList(): void {
  loading.value = true;
  initData(queryParams.value).then(response => {
    onlineList.value = response.data;
    total.value = response.data.length;
    loading.value = false;
  });
}

/** 搜索按钮操作 */
function handleQuery(): void {
  pageNum.value = 1;
  getList();
}

/** 重置按钮操作 */
function resetQuery(): void {
  proxy?.resetForm("queryRef");
  handleQuery();
}

/** 强退按钮操作 */
function handleForceLogout(row: OnlineUser): void {
  proxy?.$modal.confirm('是否确认强退名称为"' + row.userName + '"的用户?').then(function () {
    return forceLogout(row.tokenId);
  }).then(() => {
    getList();
    proxy?.$modal.msgSuccess("删除成功");
  }).catch(() => {
  });
}

onMounted(() => {
  getList();
});
</script>
